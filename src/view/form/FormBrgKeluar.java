    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        ComboItem barang = (ComboItem) comboBarang.getSelectedItem();
        String tanggal = inputTanggal.getText();
        int jumlah = (int) inputJumlah.getValue();
        int idBarang = barang.getValue();
        
        boolean verified =  verify(tanggal, jumlah);
        
        if(verified) {
            Transaksi data = new Transaksi();
            
            data.idBarang = idBarang;
            data.jumlah = jumlah;
            data.tanggal = tanggal;
            
            services.addData(data);
            services.updateJumlah(data.idBarang, data.jumlah, true);
            services.getData();
            
            this.dispose();
            clear();
        }
    }//GEN-LAST:event_SimpanActionPerformed

