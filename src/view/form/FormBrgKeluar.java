    public boolean verify(String tanggal, int jumlah) {
        boolean verified = true;
        
        Border customBorder = new LineBorder(Color.RED, 1, true);
        Border normal = new LineBorder(Color.GRAY, 1, true);
        
        SimpleDateFormat inputDateFormat = new SimpleDateFormat("dd-MM-yyy");
        Date parsed = null;
        
        try {
            parsed = inputDateFormat.parse(tanggal);
         } catch (ParseException e) {
            System.err.println("Tanggal salah: " + e.getMessage());
        }
        
        if(parsed == null) {
            inputTanggal.setBorder(customBorder);
            verified = false;
        } else {
            inputTanggal.setBorder(normal);
        }
        
        if(jumlah < 1) {
            inputTanggal.setBorder(customBorder);
            verified = false;
        } else {
            inputTanggal.setBorder(normal);
        }
        
        return verified;
    }
    
    
